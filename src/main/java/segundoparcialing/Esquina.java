public class Esquina {
    private String calleA;
    private String calleB;
    private int altura;

    public Esquina(String calleA, String calleB, int altura) {
        this.calleA = calleA;
        this.calleB = calleB;
        this.altura = altura;
    }

    public String getCalleA() {
        return calleA;
    }

    public void setCalleA(String calleA) {
        this.calleA = calleA;
    }

    public String getCalleB() {
        return calleB;
    }

    public void setCalleB(String calleB) {
        this.calleB = calleB;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
    
    
    
}
